package animal;

public class Tiger extends Animal {
    public void Eat() {
        System.out.println("Tiger eat meat");
    }

    public void Sleep() {
        System.out.println("Tiger sleep");
    }

    public void Walk() {
        System.out.println("Tiger walk");
    }

    public void Run() {
        System.out.println("Tiger run");
    }

    public void Roar() {
        System.out.println("Tiger roar");
    }

}
